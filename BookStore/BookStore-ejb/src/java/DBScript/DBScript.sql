DROP TABLE USERS;
DROP TABLE CATEGORY;
DROP TABLE BOOK;
DROP TABLE COMMENT;
DROP TABLE ORDERS;
DROP TABLE ORDER_LINE;

CREATE TABLE "ADMIN".USERS
(
	ID INTEGER not null primary key,
	LOGINNAME VARCHAR(255) not null unique,
	EMAIL VARCHAR(100) not null unique,
	PASSWORD VARCHAR(512) not null,
	PHONE VARCHAR(20) not null,
	FULLNAME VARCHAR(255) not null,
	BIRTHDAY DATE not null,
	CREATEDATE DATE not null
);

CREATE TABLE "ADMIN".CATEGORY
(
    ID INTEGER not null primary key,
    "NAME" VARCHAR(255) not null unique,
    SORTORDER VARCHAR(255) not null
);

CREATE TABLE "ADMIN".BOOK
(
    ID INTEGER not null primary key,
    TITLE VARCHAR(255) not null unique,
    DESCRIPTION VARCHAR(512) not null,
    AUTHOR VARCHAR(255) not null,
    PUBLISHER VARCHAR(255) not null,
    PUBLISHDATE DATE not null,
    PRICE DOUBLE not null,
    PHOTO VARCHAR(512) not null,
    RATINGCOUNT INTEGER not null,
    RATINGVALUE INTEGER not null,
    CATEGORY_ID INTEGER not null
);

ALTER TABLE BOOK
ADD FOREIGN KEY(CATEGORY_ID)
REFERENCES CATEGORY(ID);

CREATE TABLE "ADMIN".COMMENT
(
    ID INTEGER not null primary key,
    RATING INTEGER not null,
    CONTENT VARCHAR(512) not null,
    USER_ID INTEGER not null,
    BOOK_ID INTEGER not null,
    COMMENT_DATE DATE not null
);

ALTER TABLE COMMENT
ADD FOREIGN KEY(USER_ID)
REFERENCES USERS(ID);

ALTER TABLE COMMENT
ADD FOREIGN KEY(BOOK_ID)
REFERENCES BOOK(ID);

CREATE TABLE "ADMIN".ORDERS
(
    ID INTEGER not null primary key,
    ORDER_DATE DATE not null,
    TOTAL_AMOUNT DOUBLE not null,
    USER_ID INTEGER not null,
    SHIPPING_ADDRESS VARCHAR(512) not null
);

ALTER TABLE ORDERS
ADD FOREIGN KEY(USER_ID)
REFERENCES USERS(ID);

CREATE TABLE "ADMIN".ORDER_LINE
(
	ID INTEGER not null primary key,
	ORDER_ID INTEGER not null,
	BOOK_ID INTEGER not null,
	PRICE DOUBLE not null,
	QUANTITY INTEGER not null
);

ALTER TABLE ORDER_LINE
ADD FOREIGN KEY(ORDER_ID)
REFERENCES ORDERS(ID);

ALTER TABLE ORDER_LINE
ADD FOREIGN KEY(BOOK_ID)
REFERENCES BOOK(ID);

